using Robust.Client.UserInterface.CustomControls;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.PlayingCard.UI
{
    [GenerateTypedNameReferences]
    public partial class PlayingCardDeckWindow : DefaultWindow
    {
        private static bool IsSpinValid(int n)
        {
            return n is >= 1 and <= 10;
        }

        public PlayingCardDeckWindow()
        {
            RobustXamlLoader.Load(this);
            PickupCardAmount.IsValid = IsSpinValid;
        }
    }
}
